	static int SignOf1(int n) {
		int sign=0;
		
		if(n>0) {
			sign = 1;
		}else if(n<0) {
			sign = -1;
		}else {
			sign = 0;
		}
		
		return sign;
	}
	
	static int min(int a, int b, int c) {
		int min = a;
		if(b<min) 
			min=b;
		if(c<min)
			min=c;
		
		return min;
	}

	static int med(int a, int b, int c) {
		if(a>=b) {
			if(b>=c)
				return b;
			else if(a>=c) {
				return c;
			}else {
				return a;
			}
		}else if(a > c) {//a < b
			return a;
		}else if(b < c) {//c > a /a < b
			return b;
		}else {//c > a /a < b /b > c
			return c;
		}
		
		
	}
	static int sumUp(int x) {
		int sum = 0;
		for(int i=1; i<=x; i++) {
			sum += i;
		}
		return sum;
	}
	
