	static void putChar(char c,int n) {
		while(n-- > 0)
			System.out.print(c);
	}
	static void putStart(int n) {
		putChar('*',n);
	}
	static int random(int max, int min) {
		if(max<=min)
			return min;
		else {
			Random rand = new Random();
			return min + rand.nextInt(max-min+1); 
		}
	}
	
